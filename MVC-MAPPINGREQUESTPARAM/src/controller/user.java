package controller;

public class user {
private String name;
private int age;
private String type;
public String getName() {
	return name;
}
public int getAge() {
	return age;
}
public String getType() {
	return type;
}
public user(String name, int age, String type) {
	super();
	this.name = name;
	this.age = age;
	this.type = type;
}

}
